@pytest.mark.asyncio
async def test_list_properties_with_text_search():
    """
    Tests listing properties with full-text search parameters.
    Checks that MongoDB is queried for matching IDs, which are then fetched from Postgres.
    """
    db_session = MagicMock()
    mongo_db = MagicMock()
    
    match_id = uuid4()

    with patch("services.property.PropertyRepository") as mock_postgres_repo_class, \
         patch("services.property.MongoSearchRepository") as mock_mongo_repo_class:
         
        mock_mongo_repo = mock_mongo_repo_class.return_value
        mock_mongo_repo.search_properties_ids = AsyncMock(return_value=[match_id])

        db_prop = Property(
            id=match_id,
            title="PSG tech nearby apartment"
        )
        
        # Setup mock db response
        mock_db_result = MagicMock()
        mock_db_result.scalars.return_value.all.return_value = [db_prop]
        
        mock_postgres_repo = mock_postgres_repo_class.return_value
        mock_postgres_repo.db.execute = AsyncMock(return_value=mock_db_result)

        # Execute
        service = PropertyService(db_session, mongo_db)
        results, count = await service.list_properties(search="PSG tech", limit=10)

        # Assertions
        assert count == 1
        assert results[0].id == match_id
        assert results[0].title == "PSG tech nearby apartment"
        mock_mongo_repo.search_properties_ids.assert_called_once_with("PSG tech", limit=10, skip=0)


