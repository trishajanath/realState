    prop_create = PropertyCreate(
        title="3 BHK Villa in RS Puram",
        property_type="Villa",
        listing_type="Sale",
        price=Decimal("15000000.00"),
        area_sqft=Decimal("2400.00"),
        bedrooms=3,
        bathrooms=4,
        latitude=11.0112,
        longitude=76.9458,
        locality_id=locality_id,
        city="Coimbatore",
        state="Tamil Nadu",
        source="Scraper",
        listing_url="http://example.com/listings/1"
    )

    with patch("services.property.AIService") as mock_ai_class, \
         patch("services.property.PropertyRepository") as mock_postgres_repo_class, \
         patch("services.property.MongoSearchRepository") as mock_mongo_repo_class:
         
        # Create standard return record
        db_prop = Property(
            id=uuid4(),
            title="3 BHK Villa in RS Puram",
            property_type="Villa",
            listing_type="Sale",
            price=Decimal("15000000.00"),
            area_sqft=Decimal("2400.00"),
            bedrooms=3,
            bathrooms=4,
            latitude=11.0112,
            longitude=76.9458,
            locality_id=locality_id,
            city="Coimbatore",
            state="Tamil Nadu",
            source="Scraper",
            listing_url="http://example.com/listings/1",
            ai_description="Premium RS Puram villa details generated by AI.",
            ai_investment_rating="Grade: A | Analysis: Great location deal."
        )
        
        # Setup mocks behavior
        mock_postgres_repo = mock_postgres_repo_class.return_value
        mock_postgres_repo.create = AsyncMock(return_value=db_prop)
        
        mock_mongo_repo = mock_mongo_repo_class.return_value
        mock_mongo_repo.index_property = AsyncMock()

        mock_ai = mock_ai_class.return_value
        mock_ai.generate_property_description = AsyncMock(return_value="Premium RS Puram villa details generated by AI.")
        mock_ai.evaluate_property_deal = AsyncMock(return_value="Grade: A | Analysis: Great location deal.")

        # Execute
        service = PropertyService(db_session, mongo_db)
        res = await service.create_property(prop_create)
        
        # Assertions
        assert res.id is not None
        assert res.title == "3 BHK Villa in RS Puram"
        assert res.ai_description == "Premium RS Puram villa details generated by AI."
        assert res.ai_investment_rating == "Grade: A | Analysis: Great location deal."
        
        # Verify correct dependencies were invoked
        mock_postgres_repo.create.assert_called_once()
        mock_mongo_repo.index_property.assert_called_once()
        mock_ai.generate_property_description.assert_called_once()


